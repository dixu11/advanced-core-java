    class Figury extends JPanel {

        @Override
        protected void paintComponent(Graphics g) {
            Font font = new Font("SanSerif", ITALIC, 16);
            g.setFont(font);

            g.clearRect(0, 0, getWidth(), getHeight());

            g.setColor(Color.RED);
            g.drawLine(0, getHeight() / 2, getWidth(), getHeight() / 2);
            g.setColor(Color.MAGENTA);
            g.drawRect(30, 70, 80, 90);
            g.drawString("ProstokÄ…t", 30, 200);
            g.drawRect(30, 70 + getHeight() / 2, 80, 90);

            g.setColor(Color.RED);
            g.drawOval(getWidth() / 2 - 40, 70, 80, 90);
            g.drawOval(getWidth() / 2 - 40, 70 + getHeight() / 2, 80, 90);

            Color mojKolor = new Color(36, 36, 255);
            g.setColor(mojKolor);

            g.drawArc(getWidth() / 2 - 40, 70, 80, 90, 45, 85);
            g.fillArc(getWidth() / 2 - 40, 70 + getHeight() / 2, 80, 90, 45, 85);

            Random random = new Random();

            Color losowyKolor =
                    new Color(random.nextInt(255), random.nextInt(255), random.nextInt(255));
            g.setColor(losowyKolor);

            Polygon osmiokat = new Polygon();
            double alfa = 2 * Math.PI / 8;
            int x0 = getWidth() - 100;
            int y0 = 120;

            for (int i = 0; i < 8; i++) {
                osmiokat.addPoint(x0 + (int) (50 * Math.cos(i * alfa)), y0 + (int) (50 * Math.sin(i * alfa)));
            }
            g.drawPolygon(osmiokat);
            osmiokat.translate(0, getHeight() / 2);
            g.fillPolygon(osmiokat);

            int[] x = {0, getWidth() / 2, getWidth()};
            int[] y = {0, 60, 0};
            Color k3 = new Color(100, 240, 50);
            g.setColor(k3);
            g.fillPolygon(x, y, 3);
            g.setColor(Color.black);
            g.drawPolygon(x, y, 3);

            String tytul = "Rysowanie figur geometrycznych";
            int pozycja = getWidth() / 2 - g.getFontMetrics().stringWidth(tytul) / 2;
            g.drawString(tytul, pozycja, 20);


        }
    }

