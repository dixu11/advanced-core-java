    class PanelGraficzny extends JPanel{

        @Override
        public void paintComponent(Graphics g) {
            g.setColor(Color.WHITE);
            g.fillRect(0, 0, getWidth(), getHeight());

            int szerokoscSlupka = getWidth()/10;

            for(int i = 0; i<10;i++){
                int wartosc = (int) wylosowane.getValueAt(0, i);

                Color losowyKolor = new Color(
                        random.nextInt(256),
                        random.nextInt(256),
                        random.nextInt(256)
                );

                g.setColor(losowyKolor);

                int wysokoscSlupka = getHeight()/20 * wartosc;

                g.fillRect(
                        i * szerokoscSlupka,
                        getHeight()- wysokoscSlupka,
                        szerokoscSlupka,
                        wysokoscSlupka
                );

            }
        }

    }

